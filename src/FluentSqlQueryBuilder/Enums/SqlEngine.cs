        /// </summary>
        PostgreSQL,

        /// <summary>
        /// Indicates the SQLite database engine.
        /// </summary>
        SQLite,

        /// <summary>
        /// Indicates the SQL Server database engine.
        /// </summary>
        SQLServer
    }
}
